#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a;
    a = 10;
    //The assignment operator (=) is used to assign a value to a variable
    //The left hand side of the assignment operator is called the l value
    //The right hand side of the assignment operator is called the r value
    //In this example, a is the l value and 10 is the r value
    //The l value is the target of the assignment and the r value is the source for the assignment
    //l value can only be a variable and not constants
    //r value can be both variables or constants

    return 0;
}
